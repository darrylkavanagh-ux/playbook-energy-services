/**
 * PROFESSIONAL FORECAST SERVICE — CVK-1100
 * =============================================================================
 * Issues professional trading forecasts with full legal responsibility chain.
 *
 * CORE PRINCIPLE (per Darryl's instruction):
 *   "If someone uses the information the platform produces they assume
 *    responsibility for sign off fully and the forecast or any information
 *    provided does not become legal or binding in any way until this is
 *    completed and returned to the platform for confirmation where the
 *    platform will identify the qualified professional to ensure that person
 *    has the qualifications required to sign the relevant certificate."
 *
 * RESPONSIBILITY CHAIN:
 *   Step 1 — PLATFORM generates forecast (AI + Oracle 12-pillar)
 *   Step 2 — DISCLAIMER issued: information only, NOT binding, user assumes
 *             full responsibility for any action taken
 *   Step 3 — USER returns forecast to platform for confirmation sign-off
 *   Step 4 — PLATFORM identifies qualified professional for the domain
 *   Step 5 — QUALIFIED PROFESSIONAL verifies credentials + signs certificate
 *   Step 6 — CERTIFICATE issued: legally valid, traceable, audit-trailed
 *   Step 7 — V10 gate records the signed certificate as A1 evidence
 *
 * FORECAST TYPES:
 *   TRADING_TIP       — Short actionable signal (BUY/SELL/HOLD + entry/SL/TP)
 *   MARKET_FORECAST   — Directional bias for symbol over 1H/4H/D/W timeframe
 *   RISK_ASSESSMENT   — Probability and risk/reward analysis
 *   SECTOR_OUTLOOK    — Multi-asset class view (forex/crypto/stock sector)
 *   MACRO_BRIEF       — Central bank stance + macro environment summary
 *
 * LEGAL FRAMEWORK:
 *   - MiFID II Article 24: Best execution + suitability
 *   - FCA COBS 12: Recommendations and research
 *   - ESMA Guidelines on product governance
 *   - Consumer Duty (FCA PS22/9): fair value + consumer outcomes
 *   - GDPR Article 22: automated decision-making disclosure
 *
 * CVK-1100: Every forecast is scored, hashed, and gated before publication.
 */

import crypto from 'crypto';

// ── Types ─────────────────────────────────────────────────────────────────────

export type ForecastType =
  | 'TRADING_TIP'
  | 'MARKET_FORECAST'
  | 'RISK_ASSESSMENT'
  | 'SECTOR_OUTLOOK'
  | 'MACRO_BRIEF';

export type ForecastStatus =
  | 'DRAFT'           // Generated by AI, not yet returned for sign-off
  | 'PENDING_SIGNOFF' // User returned to platform, awaiting professional
  | 'UNDER_REVIEW'    // Qualified professional is reviewing
  | 'SIGNED'          // Professional signed — legally valid
  | 'REJECTED'        // Professional rejected — not to be used
  | 'EXPIRED';        // Expired before sign-off (24h for trading tips)

export type ResponsibilityParty =
  | 'AI_PLATFORM'     // Initial generation
  | 'END_USER'        // User who received and may act on the information
  | 'QUALIFIED_PROFESSIONAL'; // Licensed signer

export type ForecastTimeframe = '1H' | '4H' | 'D' | 'W' | 'M';

export interface ResponsibilityLink {
  party:        ResponsibilityParty;
  timestamp:    string;
  action:       string;
  description:  string;
  ip_hash?:     string;   // SHA-256 hash of IP — privacy-compliant
  signature?:   string;   // SHA-256 of party + timestamp + forecast_id
  credential?:  string;   // Professional credential ID (for QUALIFIED_PROFESSIONAL)
}

export interface ForecastDisclaimer {
  version:              string;    // e.g. "ORB-DISCLAIMER-v2.1"
  issued_at:            string;
  language:             string;    // ISO 639-1 e.g. "en"

  // Core statements
  not_financial_advice: string;
  user_responsibility:  string;
  no_guarantee:         string;
  risk_warning:         string;
  regulatory_status:    string;
  ai_limitation:        string;
  sign_off_required:    string;
  gdpr_article_22:      string;

  // User acknowledgement (must be explicitly accepted before user acts)
  user_must_acknowledge: string[];
  acceptance_required:   boolean;
}

export interface ProfessionalForecast {
  // Identity
  forecast_id:      string;          // FCST-{type}-{ts}-{random6}
  forecast_type:    ForecastType;
  status:           ForecastStatus;

  // Subject
  symbol:           string;
  asset_class:      'forex' | 'crypto' | 'stock' | 'macro';
  timeframe:        ForecastTimeframe;

  // Forecast content
  direction:        'BULLISH' | 'BEARISH' | 'NEUTRAL';
  conviction:       'HIGH' | 'MEDIUM' | 'LOW';
  oracle_score:     number;          // 0–100 from Oracle 12-pillar

  // Trading tip fields (when type = TRADING_TIP)
  tip?: {
    action:         'BUY' | 'SELL' | 'HOLD';
    entry_price:    number;
    stop_loss:      number;
    take_profit_1:  number;
    take_profit_2:  number;
    risk_reward:    number;
    position_size_pct: number;
    pip_value?:     number;
  };

  // Forecast narrative
  summary:          string;          // 1–2 sentence human-readable summary
  rationale:        string[];        // Bullet points: key drivers
  risks:            string[];        // Key risk factors
  key_levels:       { label: string; price: number; type: 'support' | 'resistance' | 'target' }[];

  // Macro context (always populated)
  macro_context: {
    central_bank_stance: string;
    smart_money_bias:    string;
    market_session:      string;
    volatility_regime:   string;
  };

  // V10 certification
  signal_id:        string;          // source signal ID
  veritech_cert_id: string;
  data_hash:        string;          // SHA-256 of forecast content

  // Legal / responsibility chain
  disclaimer:       ForecastDisclaimer;
  responsibility_chain: ResponsibilityLink[];

  // Timestamps
  generated_at:     string;
  expires_at:       string;         // 24H for TRADING_TIP, 7D for MARKET_FORECAST
  signed_at?:       string;
  signed_by?:       string;         // Qualified professional credential ID

  // V10 gate
  v10_certified:    boolean;
  cert_id?:         string;
}

export interface SignOffRequest {
  forecast_id:      string;
  requester_id:     string;         // Who is requesting sign-off
  requester_type:   'USER' | 'SYSTEM';
  reason:           string;
  user_acknowledgement: boolean;    // MUST be true — user accepted disclaimer
  submitted_at:     string;
  ip_hash?:         string;
}

export interface SignOffResult {
  success:          boolean;
  forecast_id:      string;
  status:           ForecastStatus;
  assigned_professional?: {
    credential_id:  string;
    name:           string;
    qualification:  string;
    domain:         string;
  };
  message:          string;
  next_step:        string;
}

